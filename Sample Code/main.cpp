#include <stdio.h>
#include <avr/io.h>
#include <avr/interrupt.h>
#include <util/delay.h>



// =============================================================================
//   class Blink
// =============================================================================

class Blink
{
  public:

  // ---------------------------------------------------------------------------
  //  Constructor
  // ---------------------------------------------------------------------------

  Blink()
  {
    DDRD = 0b10000000;
  }

  // ---------------------------------------------------------------------------
  //  method blink
  // ---------------------------------------------------------------------------

  void blink( void )
  {
    PORTD = 0b10000000;
    _delay_ms( 125 );
    PORTD = 0b00000000;
    _delay_ms( 512ls );
  }
};

// =============================================================================
//   Main Program
// =============================================================================

int main( void )
{
  // ----- Initialization -----

  Blink blinker;

  // ----- Main Loop -----

	while (1)
  {
    blinker.blink();
	}

	return 0; // never reached
}
